    private void button_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_inputActionPerformed
        // TODO add your handling code here:
        try {
            transaksi tran = new transaksi();
            text_totalbayar .setText(""+tran.total_bayar);
            this.stat = k.getCon().prepareStatement("insert into transaksi values(?,?,?,?,?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, tran.nama_pelanggan);
            stat.setInt(3, tran.id_makanan);
            stat.setString(4, tran.tanggal);
            stat.setString(5, tran.nama_makanan);
            stat.setInt(6, tran.harga);
            stat.setInt(7, tran.jumlah_beli);
            stat.setInt(8, tran.total_bayar);
            int pilihan = JOptionPane.showConfirmDialog(null, "Tanggal " + tran.tanggal+
                    "\nNama Pelanggan: " + tran.nama_pelanggan+
                    "\nPembelian: " + tran.jumlah_beli + " "
                    + tran.nama_makanan+"\n Total Bayar: " + tran.total_bayar + "\n",
                    "Tambahkan Transaksi?", 
                    JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                this.stat.executeUpdate();
                refreshTable();
            } else if(pilihan == JOptionPane.NO_OPTION){
                refreshTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_button_inputActionPerformed

