    class transaksi extends Menu_Transaksi{
        int id_transaksi,id_makanan,harga,jumlah_beli,total_bayar;
        String nama_pelanggan,nama_makanan,tanggal;

        public transaksi() {
            this.nama_pelanggan = text_namapelanggan.getText();
            String combo = combo_idmasakan.getSelectedItem().toString();
            String[] arr = combo.split(":");
            this.id_makanan = Integer.parseInt(arr[0]);
            try {
                Date date = text_tanggal.getDate();
                DateFormat dateformat = new SimpleDateFormat("YYYY-MM-dd");
                this.tanggal = dateformat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            this.nama_makanan = arr[1];
            this.harga = Integer.parseInt(arr[2]);
            this.jumlah_beli = Integer.parseInt(text_jumlahbeli.getText());
            this.total_bayar = this.harga * this.jumlah_beli;
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Nama Pelanggan");
        model.addColumn("ID Makanan");
        model.addColumn("Tanggal");
        model.addColumn("Nama Makanan");
        model.addColumn("Harga");
        model.addColumn("Jumlah Beli");
        model.addColumn("Total Bayar");
        table_transaksi.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("Select * from transaksi");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                  rs.getString(1),
                  rs.getString(2),
                  rs.getString(3),
                  rs.getString(4),
                  rs.getString(5),
                  rs.getString(6),
                  rs.getString(7),
                  rs.getString(8),
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_idtransaksi.setText("");
        text_namapelanggan.setText("");
        text_jumlahbeli.setText("");
        text_totalbayar.setText("");
        
    }
    
    public void refreshCombo(){
        try {
            this.stat = k.getCon().prepareStatement("Select * from makanan "
                    + "where status = 'TERSEDIA'");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                combo_idmasakan.addItem(rs.getString("id_makanan")+":"
                        + rs.getString("nama_makanan") + ":" +rs.getString("harga"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
